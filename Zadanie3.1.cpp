
#include <iostream>

using namespace std;

int main()
{
    float x;
    float y;

    cout << "Podaj liczbê x:" << endl;
    cin >> x;

    cout << "Podaj liczbê y:" << endl;
    cin >> y;

    cout << x << " + " << y << " = " << x + y << endl;
    cout << x << " - " << y << " = " << x - y << endl;
    cout << x << " * " << y << " = " << x * y << endl;
    cout << x << " / " << y << " = " << x / y << endl;

    return 0;
}